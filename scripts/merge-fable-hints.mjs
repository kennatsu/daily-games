// Fable 5 が生成した hints-batch-*.json をマージして stock-hints.mjs を更新
// 使い方: node scripts/merge-fable-hints.mjs

import { readFileSync, writeFileSync, readdirSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";
import { formatChartHint } from "./analyze-chart.mjs";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const input = JSON.parse(readFileSync(join(root, "scripts/hints-input.json"), "utf8"));
const inputByTicker = Object.fromEntries(input.map((s) => [s.ticker, s]));

const batches = readdirSync(join(root, "scripts"))
  .filter((f) => f.startsWith("hints-batch-") && f.endsWith(".json"))
  .sort();

if (!batches.length) {
  console.error("No hints-batch-*.json found. Run Fable 5 generation first.");
  process.exit(1);
}

const merged = {};
for (const file of batches) {
  const arr = JSON.parse(readFileSync(join(root, "scripts", file), "utf8"));
  for (const item of arr) merged[item.ticker] = item;
}

const hints = {};
for (const stock of input) {
  const fable = merged[stock.ticker];
  if (!fable) throw new Error(`Missing Fable hints for ${stock.ticker}`);

  const events = fable.events.map((e) => {
    const move = stock.moves.find((m) => m.index === e.index && m.type === e.type);
    if (!move) throw new Error(`${stock.ticker}: event index ${e.index} not in moves`);
    return {
      index: e.index,
      type: e.type,
      explain: e.explain,
      hint: formatChartHint(move, e.explain),
    };
  });

  hints[stock.ticker] = { earn: fable.earn, moat: fable.moat, clue: fable.clue, events };
}

const lines = Object.entries(hints).map(([ticker, h]) => {
  const eventsStr = h.events.map((e) =>
    `{ index: ${e.index}, type: ${JSON.stringify(e.type)}, explain: ${JSON.stringify(e.explain)}, hint: ${JSON.stringify(e.hint)} }`
  ).join(", ");
  return `  "${ticker}": { earn: ${JSON.stringify(h.earn)}, moat: ${JSON.stringify(h.moat)}, clue: ${JSON.stringify(h.clue)}, events: [${eventsStr}] }`;
}).join(",\n");

writeFileSync(join(root, "scripts/stock-hints.mjs"), `// Generated by merge-fable-hints.mjs\nexport const HINTS = {\n${lines}\n};\n`);
console.log(`merged ${Object.keys(hints).length} tickers from ${batches.length} batch files`);
